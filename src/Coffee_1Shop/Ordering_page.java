    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String name = OrderName.getText();
        String coffee = Coffee.getSelectedItem().toString();
        String qty = Qty.getText();
        String price = Pri.getText();

        if (name.isEmpty() || coffee.equals("Select") || qty.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields.", "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (!qty.matches("\\d{1,2}")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid quantity (1 to 99 only).", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        model.addRow(new Object[]{name, coffee, qty, price});

    }//GEN-LAST:event_jButton1ActionPerformed

    private void ProceedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProceedActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for (int i = 0; i < model.getRowCount(); i++) {
            Object nameObj = model.getValueAt(i, 0);
            Object coffeeObj = model.getValueAt(i, 1);
            Object qtyObj = model.getValueAt(i, 2);
            Object priceObj = model.getValueAt(i, 3);
            
            

            if (nameObj == null && coffeeObj == null && qtyObj == null && priceObj == null) {
                continue;    
            }
            
            if (nameObj == null || coffeeObj == null || qtyObj == null || priceObj == null) {
                JOptionPane.showMessageDialog(this, "One or more rows have empty fields. Please complete all fields.");
                return;
            }
       
            String name = nameObj.toString();
            String coffee = coffeeObj.toString();
            int quantity = Integer.parseInt(qtyObj.toString());
            
            String priceStr = priceObj.toString().replaceAll("[^\\d.]", "");
            if (priceStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Invalid price value at row " + (i + 1));
                return;
            }
            double price;
            try {
                price = Double.parseDouble(priceStr);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Error parsing price at row " + (i + 1) + ": " + priceObj.toString());
                return;
            }
   
            boolean success = DatabaseConnection.insertOrder(name, coffee, quantity, price);
            if (!success) {
                JOptionPane.showMessageDialog(this, "Failed to insert order: " + name);
            }
        }
        JOptionPane.showMessageDialog(this, "Orders submitted successfully!");
        model.setRowCount(0);
    }//GEN-LAST:event_ProceedActionPerformed


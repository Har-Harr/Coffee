    private void CButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CButtonActionPerformed
        // TODO add your handling code here:  
        String newPassword = NPass.getText();
        String confirmPassword = new String(CPass.getPassword());
        
        if (newPassword.equals(confirmPassword)) {
            String storedPasswordHash = DatabaseHelper.getStoredPassword(email);
            
            String hashedPassword;
            
            if (storedPasswordHash != null && (storedPasswordHash.startsWith("$2a$") || storedPasswordHash.startsWith("$2b$"))) {
                hashedPassword = BCrypt.hashpw(newPassword, BCrypt.gensalt());
            } else {
                hashedPassword = BCrypt.hashpw(newPassword, BCrypt.gensalt());
            }
            boolean success = DatabaseHelper.updatePassword(email, hashedPassword);
            if (success) {
                    JOptionPane.showMessageDialog(this, "Password successfully updated!");
                    this.dispose();
                    new user_login().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update password. Please try again.");
                }    
            } else {
                JOptionPane.showMessageDialog(this, "Passwords do not match. Please try again.");
            }
    }//GEN-LAST:event_CButtonActionPerformed


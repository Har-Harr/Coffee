    private void CVCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CVCActionPerformed
        // TODO add your handling code here:
        String enteredCode = VC.getText().trim();
        String storedCode = DatabaseHelper.getVerificationCode(email);

        if (storedCode != null && storedCode.equals(enteredCode)) {
            JOptionPane.showMessageDialog(this, "Verification successful.");

            forgot_pass2_page newPasswordPage = new forgot_pass2_page(email);
            newPasswordPage.setVisible(true); 
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid verification code. Please try again.");
        }
    }//GEN-LAST:event_CVCActionPerformed

